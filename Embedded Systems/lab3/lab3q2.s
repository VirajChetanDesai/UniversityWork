Reset_Handler
	LDR R0,=NUM
	LDR R1,[R0]
	LDR R7,=DEST
	
	MLA R1,R1,R1,R1
	MOV R2,#2
	MOV R3,#0
LOOP SUB R1,R1,R2
	ADD R3,R3,#1
	CMP R1,R2
	BHS LOOP
	STR R3,[R7]
STOP B STOP
