	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC
	LDR R1,[R0]
	MOV R4,#0 ; MULTIPLIER
	MOV R2,#0
LOOP
	ADD R4,R4,#1
	AND R3,R1,#0XF
	MUL R5,R3,R4
	ADD R2,R2,R5
	CMP R4,#8
	BNE LOOP
	
	LDR R10,=DEST
	STR R2,[R10]
	
STOP B STOP
SRC DCD 0X11111111
	AREA data,DATA,READWRITE
DEST DCD 0
	END