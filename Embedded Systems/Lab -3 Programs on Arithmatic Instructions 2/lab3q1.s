	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors 
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=DEST1
	LDR R1,=DEST2
	
	MOV R2,#0
	LDR R3,=QUO
	LDR R4,=REM
	LDR R5,[R0]
	LDR R6,[R1]
LOOP SUB R5,R5,R6
	ADD R2,R2,#1
	CMP R5,R6
	BHS LOOP
	
	STR R2,[R3]
	STR R5,[R4]
STOP B STOP
DEST1 DCD 46
DEST2 DCD 9
	AREA data, DATA, READWRITE
QUO DCD 0
REM DCD 0
	END